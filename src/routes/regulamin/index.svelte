	<br/>
	<h2><br/>
		Art 1. Postanowienia ogólne<br/>
	</h2><br/>
	<br/>
	§1. Gracz logując się na serwer akceptuje regulamin i oświadcza, że dołoży wszelkich starań by go przestrzegać.<br/>
	<br/>
	§2. Podczas nałożenia blokady na konto gracza przedmioty związane z kontem mogą zostać usunięte.<br/>
	<br/>
	§3. Nieznajomość regulaminu nie zwalnia gracza z odpowiedzialności za popełnione czyny<br/>
	<br/>
	§4. Administracja ma prawo ukarać gracza za czyn nieuwzględniony w regulaminie który negatywnie wpływa lub może wpłynąć na serwer<br/>
	<br/>
	§5. Oszustwa to znaczy oferowanie przedmiotu / usługi / pomocy a potem nie udzielanie tego przedmiotu / usługi / pomocy po uzgodnionej lub braku spełnienia danych warunków uzgodnionych przez obu graczy nie podlega zwrocie przedmiotów osobie która została oszukana przez osobe która zaproponowała dany przedmiot / usługe / pomoc<br/>
	<br/>
	§6. Kopiowanie i używanie regulaminu serwera SpaceMC jest zabronione przez jednostki trzecie<br/>
	<br/>
	§7. Regulamin może być zmieniony bez poinformowania graczy<br/>
	<br/>
	§8. Administrator może ukarać gracza za wstawianie / tworzenie treści niezgodnych z polskim prawem<br/>
	<hr/><br/>
	<h2><br/>
	Art 2. Administracja<br/>
	</h2><br/>
	<br/>
	§1. Administrator nie ma zobowiązania podania powodu dlaczego gracz jest sprawdzany.<br/>
	<br/>
	§2. Administracja ma prawo przejrzeć wszystkie pliki związane z Minecraft oraz te, które mogą zawierać potencjalne programy/modyfikacje ułatwiające rozgrywke [niedozwolone modyfikacje] bez zgody gracza.<br/>
	<br/>
	§3. Administracja ma prawo poprosić gracza w każdej chwili o udostępnienie ss'ów oraz folderów/plików które mogą zawierać potencjalne programy/modyfikacje ułatwiające rozgrywke. Gracz jest zobowiązany do wykonania czynności.<br/>
	<br/>
	§4. Administracja nieuczestnicząca w rozgrywce nie ingeruje w rozgrywkę na serwerze.<br/>
	<br/>
	§5. Administracja nie sprzedaje i nie rozdaje przedmiotów. Uzyskanych na trybie kreatywnym<br/>
	<br/>
	§6. Administracja nie odpowiada za szkody poniesione na koncie w skutek kradzieży konta.<br/>
	<br/>
	§7. Administracja ma prawo wymagać od graczy zainstalowania programów: Discord (może być w wersji przeglądarkowej), TeamSpeak, TeamViewer, 7-zip, AnyDesk i Paladin w celu wykrycia niedozwolonych modyfikacji.<br/>
	<br/>
	§8. Administrator może ukarać graczy w swój własny sposób(Kara nie będąca Mute/Ban/BanIP) dając graczowi karanemu wybór normalna kara lub kara administratora która zostanie poprzednio zgłoszona do zarządu z wyjaśnieniem plusów tego ukarania<br/>
	<br/>
	§9. Administrator nie może nigdy używać kar z Art 2 §8 by mieć z tego korzyść na głównym świecie kara nie może być nigdy wykorzystywana do wzbogacania się administratora na głównym świecie<br/>
	<hr/><br/>
	<h2><br/>
	<br/>
	Art 3. Gracze<br/>
	</h2><br/>
	<br/>
	§1. Zakazane jest używanie Zhackowanych klientów gry (np. Huzuni, Nodus, Resilience, itp.) - ban 30 dni.<br/>
	<br/>
	§2. Dozwolone modyfikacje: ReisMinimap oraz Optifine + Shadery. (To znaczy modyfikacje które nie dających przewagę nad graczami typu XRay / Wallhack / Macro / FullBright itp. itd.).<br/>
	<br/>
	§3. Używanie wersji Minecrafta które ułatwiają rozgrywke poprzez modyfikowanie plików gry / pamięci gry / manipulują pakietami wysyłanymi do serwera np. : Wurst Huzuni Nodus Jigsaw jest nie dozwolone<br/>
	<br/>
	§4. Używanie resource packów które są uznawane jako xray to znaczy gracz może wizualnie w grze widzieć przez bloki innych graczy surowce itp jest zabronione<br/>
	<br/>
	§5. Używanie błędów serwera jest karane bez poinformowania administratora w celu demonstracji jest karane zależnie od administratora takie błędy należy niezwłocznie zgłaszać na githubie https://github.com/SpaceMC-EU/SpaceMC-Bugi/issues<br/>
	<br/>
	§6. Brak kooperacji z administracją serwera jest karane<br/>
	<br/>
	§7. Jest zakaz tworzenia multikont bez powodu typu (zmiana nicku,skradzione konto premium itp itd) jest karane to banem permanetnym na adres ip na całą edycje serwera<br/>
	<br/>
	§8. Zakaz udostępniania konta innym osobom takie konta są blokowane permanentnie.<br/>
	<br/>
	§9. Używanie obraźliwych i wulgarnych pseudonimów na serwerze grozi banem permanetnym na ten konkretny pseudonim. (chyba że adres ip użył tego psuedonimu przez co grozi mu ban na ip)<br/>
	<br/>
	§10. Współpraca z cheaterem grozi blokadą konta, wyczyszczeniem ekwipunku i usunięciem działki, każdy cheater powinien być niezwłocznie zgłoszony administracji serwera.<br/>
	<br/>
	§11. Gdy jest się podejrzanym o złamanie regulaminu przenoszenie ekwipunku do innych graczy / miejsc skutkuje czyszczeniem ekwipunku i działki tego gracza<br/>
	<br/>
	§12. Wylogowanie się podczas sprawdzania skutkuje banem zależnym od podejrzeń administratora.<br/>
	<br/>
	§13. Przyznanie się do niedozwolonych modyfikacji pozwala na otrzymanie bana tymczasowego<br/>
	<br/>
	§14. Zakaz szerzenia błędnych / nieprawdziwych informacji na temat serwera.<br/>
	<br/>
	§15. Zakaz wzbogacania sie kosztem serwera, zakaz sprzedawania kont, przedmiotów w grze za realną walutę.<br/>
	<br/>
	§16. Oszukiwanie i/lub wprowadzanie administracji w błąd może skutkować banem permanentnym(Zależne od administratora).<br/>
	<br/>
	§17. Gracz ma obowiązek mieć włączony czat, w przeciwnym wypadku może zostać zbanowany z powodu braku odzewu podczas sprawdzania w celu wykrycia niedozwolonych modyfikacji.<br/>
	<br/>
	§18. Gracz dodaje oraz dołącza osoby do działki na własną odpowiedzialność.<br/>
	<br/>
	§19. Zakaz bezsensownego griefowania terenu dookoła działki, jeśli celowo będziesz robił np. tzw. wulkany możesz zostać ukarany banem (zależne od szkód na terenie). (Tutaj też warto zauważyć jeśli gracz nie ma działki grief jest dozwolony)<br/>
	<br/>
	§20. Zakaz kompletnego używania programu zmieniającego ip lub przekierowania swojego łączenia z serwerem przez inne proxy<br/>
	<br/>
	§21. Oszukiwanie graczy może być karane usuwaniem całego dobytku konta lub karą dostępną dla administracji (bez zwrotu przedmiotów) jeśli gracz lub administrator uzbierał wystarczające dowody na temat oszustwa.<br/>
	<br/>
	§22. Zakazane jest tworzenie sytuacji gdzie Gracz jest uwięziony nie może wyjść i jest w pułapce równocześnie nie mogąc bić się z napastnikiem / napastnikami.<br/>
	<br/>
	§23. Zakazane jest używania komendy /sethome na czyjejś działce i w regionie 128 kratek liczonego od środka działki (do której nie jest się dodanym) w takim wypadku gdy gracz ma podejrzenia na temat tego może uwięzić gracza i zabijać go w okolicach jego lokacji zapisanej przy użyciu /home<br/>
	<br/>
	§24. Pokazywanie bugów innym graczom jest nie dozwolone.<br/>
	<br/>
	§25. Nie dozwolone jest tworzenie maszyn do "afkowania" to jest omijanie systemu serwerowego który wyrzuca za bycie nieczynnym na serwerze.<br/>
	<br/>
	§25. Zakazuje się działania na nerwach lub zdrowiu psychicznym administracji<br/>
	<hr><br/>
	<h2><br/>
	Art 4. Czat<br/>
	</h2><br/>
	<br/>
	§1. Obraza graczy / administracji / serwera / rodziny graczy lub administracji grozi mutem lub banem.<br/>
	<br/>
	§2. Spamowanie / floodowanie na chacie / helpopie grozi mutem lub banem.<br/>
	<br/>
	§3. Reklamowanie innych serwerów / stron internetowych / filmów / treści pornograficznych grozi mutem lub banem.<br/>
	<br/>
	§4. Udostępnianie danych osobowych i prywatnych (nazwiska, numery telefonów, zdjęcia, Facebooki itp.) grozi mutem lub banem.<br/>
	<br/>
	§5. Zakaz ogłaszania ofert niezwiązanych z grą Minecraft na czacie globalnym.<br/>
	<br/>
	§6. Zakaz przekręcania nicków administracji i szydzenia z niej.<br/>
	<br/>
	§7. Udostępnianie adresów IP graczy grozi banem.<br/>
	<br/>
	§8. Jakiekolwiek groźby skierowane w kierunku graczy/administracji mogą być karane banem.<br/>
	<br/>
	§9. Kłócenie się z administratorem na temat słuszności kary jest zakazane na czatach ogólnych względem tego można się zgłosić na serwer discorda według wzoru na odpowiednim kanale w celu rozpatrzenia ukarania w przeciwnym razie gdy gracz się kłóci karą jest 5 dni mute.<br/>
	<hr><br/>
	<h2><br/>
	Art 5. Taryfikator<br/>
	</h2><br/>
	<br/>
	§1. Obraza / Prowokacja / Spam na czacie - Mute od 10 minut do 7 dni<br/>
	<br/>
	§2. Manipulacja pakietami odbiegającymi od Minecrafta (Od firmy Mojang) lub czytanie tych pakietów programami / urządzeniami (Chodzi tutaj o cheaty) - Ban od 7dni do 30 dni<br/>
	<br/>
	§3. Brak kooperacji z administracją - Podwójna ilość czasu kary która mogła być wymierzona<br/>
	<br/>
	§4. Wykorzystywanie błędów serwera w celu zdobycia przewagi - od 14 dni do Całkowita blokada konta(Perm)<br/>
	<br/>
	§5. Inne rzeczy nie zawarte w taryfikatorze - Zależne od administratora<br/>
	<br/>
	§6. Nadużycie rangi - Degrad<br/>
	<br/>
	§7. Kary na IP mogą być zmienione na podstawie tego czy na jednym IP po potwierdzeniu przez administratora gra pare innych osób na tym samym IP<br/>
	<br/>
	§8. Omijanie kary - Ban perm ip<br/>
	<br/>
	§9. Art 1 §1 - BanIP Perm za brak akceptacji regulaminu<br/>
	<br/>
	§10. Art 3 §1-4 - Przyznanie się do przewinienia jest równe 7 dni temp ban za pierwsze przewinienie następne się podwajają<br/>
	<br/>
	§11. Art 3 §6 - BanIP perm<br/>
	<br/>
	§12. Art 3 §24 - Zależne od administratora<br/>
	<br/>
	§13. Art 3 §25 - Zależne od administratora który ucierpiał na wynik akcji gracza<br/>
	<br/>
	§14. Proszenie innego administratora o usunięcie bądź skrócenie kary, gdy posiada on niższą/te samą rangę grozi podwojeniem kary.

	<hr><br/>
	<h2><br/>
	Art 6. Zgłaszanie bugów<br/>
	</h2><br/>
	<br/>
	§1. Bug jest to błąd serwerowy który został wprowadzony przez błąd ludzki oznacza to działanie mechanik serwera inne od przewidywanego<br/>
	<br/>
	§2. Nagroda za zgłoszone bugi liczą się tylko jeśli są zgłoszone przez system zgłaszania issues na stronie github serwera https://github.com/SpaceMC-EU/SpaceMC-Bugi/issues<br/>
	<br/>
	§3. Nagrody mogą być tylko przyznane jeśli zespół developerski uzna to za bug i zatwierdzi issue na githubie<br/>
	<br/>
	§4. Nie nagradza się zgłaszania bugów na serwerze tylko przez system github<br/>
	<br/>
	§5. Bugi dokładnie nie udokumentowane według wzoru zostaną pominięte i nie nagrodzone<br/>
	<br/>
	§6. Każde 2 dni rangi niższej można wymienić na range premium wyższą do swagger'a<br/>
	<br/>
	§7. Brak informacji na temat nicku gracza skutkuje pominięciem przyznania nagrody po czasie naprawienia i zatwierdzenia<br/>
	<br/>
	§8. Informacje na temat nicku gracza muszą być wydzielone od szablonu zgłaszania bugów najbardziej preferencyjnie na górze lub na dole szablonu oddzielone 2 enterami<br/>
	<br/>
	§9. Rodzaj rangi i ilość dni może być zdefiniowana w poniższych punktach<br/>
	<br/>
	§10. Bugi wizualne (Bugi które są tylko wyglądowe nie mechanikowe) - od 1h do 4 dni rangi Gold<br/>
	<br/>
	§11. Bugi mechaniczne (Sprawiają że jedna opcja gry jest nie dostępna lub zepsuta bo nie da się użyć tej mechaniki według sposobu użycia) - od 1 dnia do 14 dni Rangi Gold<br/>
	<br/>
	§12. Bugi dupe (Chodzi głównie o kopiowanie itemów i wykorzystywanie ich w grze później do zyskania przewagi) - od 30 dni rangi Gold'a<br/>
	<br/>
	§13. Crashery (Chodzi o bugi które sprawiają że dostęp do usług serwerowych jest ograniczony poprzez wyłączenie serwera lub go zatrzymanie) - 8 dni Rangi VIP - 14 dni Rangi Sponsor<br/>
	<h2><br/>
	<br/>
	Art 7. Dodatki na serwer<br/>
	</h2><br/>
	<br/>
	§1. Dodanie rzeczy na serwer może być tylko jeśli ilość głosów na tak będzie większa o 50% od totalnej ilości głosów na kanale propozycje serwera<br/>
	<br/>
	§2. Werdykt każdej propozycji jest podejmowany po czasie 3dni roboczych od stworzenia propozycji<br/>
	<br/>
	§3. O Werdykcie zarząd nie musi informować graczy<br/>
	<hr><br/>
	<h2><br/>
	<br/>
	Art 8. Apelacje<br/>
	</h2><br/>
	<br/>
	§1. Do apelacji o usunięcie kary może przystąpić gracz na kanale discord przeznaczonym do tego<br/>
	<br/>
	§2. Na tym kanale discord względem Art 8 §1 może się odzywać tylko banujący lub administrator który banował<br/>
	<br/>
	§3. Apelujący nie może się wypowiadać jeśli sprawa została już rozpatrzona negatywnie lub pozytywnie przez następne 8 dni <br/><br/>
	§4. Karany może zostać ułaskawiony za wpłacenie dotacji na serwer<br/>
	
